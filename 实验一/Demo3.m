figure();
I=imread('D:\视频大数据分析\Lena_256x256.bmp');
subplot(3,2,1)
imshow(I)
title('原始图像');
subplot(3,2,2);
g=imnoise(I,'gaussian',0,10);
imshow(g)
title('加入均值为0，方差分别为10的原始图像');
%进行3*3模板平滑滤波
k1=filter2(fspecial('average',3),g)/255;
subplot(3,2,3);
imshow(k1)
title('模板平滑滤波')
subplot(3,2,4);
g=imnoise(I,'gaussian',0,30);
imshow(g)
title('加入均值为0，方差分别为30的原始图像');
